import turtle

turtle.shape("turtle")
boba=turtle.Turtle()
boba.pensize(2)
boba.color('red')
boba.speed(50)
boba.penup()
boba.goto(0,200)
boba.pendown()

for i in range(80):
    boba.circle(60)
    boba.right(5)


